// Helper to get font metrics for a given size
static inline void get_font_metrics(int font_size, int* out_char_width, int* out_char_height) {
    if (font_size == 0) {
        *out_char_width = 6;  // 5 + 1 spacing
        *out_char_height = 7;
    } else if (font_size == 1) {
        *out_char_width = 9;  // 8 + 1 spacing
        *out_char_height = 12;
    } else {
        *out_char_width = 13; // 12 + 1 spacing
        *out_char_height = 16;
    }
}

// Helper function to get character index (supports space, numbers, uppercase letters)
static inline int get_char_index(char c) {
    if (c >= 32 && c <= 90) {
        return c - 32;
    } else if (c >= 97 && c <= 122) {
        // Convert lowercase to uppercase: a-z becomes A-Z
        return (c - 97) + (65 - 32); // 'a' becomes 'A', etc.
    }
    return 0; // Default to space
}

// Helper to write a pixel with RGB or grayscale, based on channel count
static inline void write_pixel(float* restrict image, int idx, int channels, float r, float g, float b) {
    if (channels >= 3) {
        image[idx] = r;
        image[idx + 1] = g;
        image[idx + 2] = b;
    } else if (channels == 1) {
        image[idx] = 0.299f * r + 0.587f * g + 0.114f * b;
    }
}

// Helper function to render a character at specific position
static inline void render_char(float* restrict image, int image_width, int image_height, int channels,
                              int x, int y, char c, int font_size, float r, float g, float b) {
    int char_idx = get_char_index(c);
    
    if (font_size == 0) { // Small font 5x7
        if (char_idx >= sizeof(font_small_5x7) / sizeof(font_small_5x7[0])) return;
        
        for (int row = 0; row < 7; row++) {
            unsigned char line = font_small_5x7[char_idx][row];
            for (int col = 0; col < 5; col++) {
                if (line & (1 << (4 - col))) {
                    int px = x + col;
                    int py = y + row;
                    if (px >= 0 && px < image_width && py >= 0 && py < image_height) {
                        int idx = (py * image_width + px) * channels;
                        write_pixel(image, idx, channels, r, g, b);
                    }
                }
            }
        }
    } else if (font_size == 1) { // Large font 8x12
        if (char_idx >= sizeof(font_large_8x12) / sizeof(font_large_8x12[0])) return;
        
        for (int row = 0; row < 12; row++) {
            unsigned short line = font_large_8x12[char_idx][row];
            for (int col = 0; col < 8; col++) {
                if (line & (1 << (7 - col))) {
                    int px = x + col;
                    int py = y + row;
                    if (px >= 0 && px < image_width && py >= 0 && py < image_height) {
                        int idx = (py * image_width + px) * channels;
                        write_pixel(image, idx, channels, r, g, b);
                    }
                }
            }
        }
    } else { // Extra large font 12x16
        if (char_idx >= sizeof(font_xlarge_12x16) / sizeof(font_xlarge_12x16[0])) return;
        
        for (int row = 0; row < 16; row++) {
            unsigned short line = font_xlarge_12x16[char_idx][row];
            for (int col = 0; col < 12; col++) {
                if (line & (1 << (11 - col))) {
                    int px = x + col;
                    int py = y + row;
                    if (px >= 0 && px < image_width && py >= 0 && py < image_height) {
                        int idx = (py * image_width + px) * channels;
                        write_pixel(image, idx, channels, r, g, b);
                    }
                }
            }
        }
    }
}

// Helper function to render text string
static inline void render_text(float* restrict image, int image_width, int image_height, int channels,
                              int x, int y, const char* text, int font_size, float r, float g, float b) {
    int char_width, char_height_dummy;
    get_font_metrics(font_size, &char_width, &char_height_dummy);
    
    int current_x = x;
    
    for (int i = 0; text[i] != '\0' && i < 32; i++) { // Limit to 32 chars for safety
        render_char(image, image_width, image_height, channels, current_x, y, text[i], font_size, r, g, b);
        current_x += char_width;
        if (current_x >= image_width) break;
    }
}

