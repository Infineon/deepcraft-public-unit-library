// Helper function to convert float to string (simple implementation)
static inline void float_to_string(float value, char* buffer, int precision) {
    int int_part = (int)value;
    float frac_part = value - int_part;
    
    // Convert integer part
    if (int_part == 0) {
        buffer[0] = '0';
        buffer[1] = '\0';
    } else {
        int len = 0;
        int temp = int_part;
        while (temp > 0) {
            len++;
            temp /= 10;
        }
        
        buffer[len] = '\0';
        temp = int_part;
        for (int i = len - 1; i >= 0; i--) {
            buffer[i] = '0' + (temp % 10);
            temp /= 10;
        }
    }
    
    // Add decimal point and fractional part
    int len = 0;
    while (buffer[len] != '\0') len++;
    
    if (precision > 0) {
        buffer[len++] = '.';
        for (int i = 0; i < precision && len < 15; i++) {
            frac_part *= 10;
            int digit = (int)frac_part;
            buffer[len++] = '0' + digit;
            frac_part -= digit;
        }
        buffer[len] = '\0';
    }
}

